package model;

/**
 *
 * @author trung
 */
public class Banner {

    public int id;
    public String img;
    public String title;
    public String desc;

    public Banner() {
    }

    public Banner(int id, String img, String title, String desc) {
        this.id = id;
        this.img = img;
        this.title = title;
        this.desc = desc;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
